//For the results "letter" filter
jQuery(".alphabetical_filter li a" ).click(function() {

  jQuery('.profile-card').hide();
  jQuery('.profile-row').hide();
  var search_letter = jQuery(this).text();



  jQuery(".alphabetical_filter li a" ).removeClass('selected');
  jQuery(this).addClass('selected');

  jQuery(".profile_name" ).each(function( index ) {

  if (jQuery(this).text().match("^"+search_letter)) {
  	jQuery(this).closest('.profile-card').show();
  	jQuery(this).closest('.profile-row').show();
    jQuery(this).closest('.profile-card').find('.profile-card').show();
  } else {
  	jQuery(this).closest('.profile-card').hide();
  	jQuery(this).closest('.profile-row').hide();
  	jQuery(this).closest('.profile-card').find('.profile-card').hide();
  }
  });

});

</script>